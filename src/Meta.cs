        [AttributeUsage(AttributeTargets.Field | AttributeTargets.Method, AllowMultiple = true)]
        public class MinMaxAttribute : Attribute
        {
            public MinMaxAttribute(ulong Min, ulong Max) { }
            public MinMaxAttribute(long Min, long Max) { }
            public MinMaxAttribute(double Min, double Max) { }
            public MinMaxAttribute(int minYear, int minMonth, int minDay, int maxYear, int maxMonth, int maxDay) { } //the unit is milliseconds

            /**The generated API will operate with time values represented in milliseconds since the Unix epoch.*/
            public MinMaxAttribute(int minYear, int minMonth, int minDay, int maxYear, int maxMonth, int maxDay, TimeSpan time_unit) { }
        }

        /**
         *  A field with values that typically center around zero but occasionally exhibit deviations towards higher magnitudes, towards positive values.
         *  The greater the deviation, the less likely the occurrence.
         *
         * if min is omitted: min = 0
         *
         * if max is omitted:
         *
         * field type       |       max
         * ======================================
         *  short           |  short.MaxValue  + min;
         *  ushort of char  |  ushort.MaxValue + min;
         *  int             |  int.MaxValue    + min;
         *  uint            |  uint.MaxValue   + min;
         *  long            |  long.MaxValue   + min;
         *  ulong           |  ulong.MaxValue  + min;
         *
         */
        [AttributeUsage(AttributeTargets.Field, AllowMultiple = true)]
        public class AAttribute : Attribute
        {
            public AAttribute(long Min_most_probable_value = 0, long Max = 0) { }
        }

        /**
         *  A field with values that typically center around zero but occasionally exhibit deviations towards higher magnitudes, towards negative values.
         *  The greater the deviation, the less likely the occurrence.
         * if max is omitted: min = 0
         *
         * if min is omitted:
         *
         * field type       |       min
         * ======================================
         *  short           |   short.MinValue  + max;
         *  ushort of char  |  -ushort.MaxValue + max;
         *  int             |   int.MinValue    + max;
         *  uint            |  -uint.MaxValue   + max;
         *  long            |   long.MinValue   + max;
         *
         */
        [AttributeUsage(AttributeTargets.Field, AllowMultiple = true)]
        public class VAttribute : Attribute
        {
            public VAttribute(long Max_most_probable_value = 0, long Min = 0) { }
        }

        /**
         *  A field with values that typically center around zero but occasionally exhibit deviations towards higher magnitudes,
         *  towards negative or positive values.
         *  The greater the deviation, the less likely the occurrence.
         *
         *  if Zero is omitted: Zero = 0
         *
         * if Amplitude is omitted:
         *
         * field type      |           min          |          max
         * ===================================================================
         * short           | -short.MaxValue  + Zero |  short.MaxValue   + Zero
         * ushort or char  | -ushort.MaxValue + Zero |  ushort.MaxValue  + Zero
         * int             | -int.MaxValue    + Zero |  int.MaxValue     + Zero
         * uint            | -uint.MaxValue   + Zero |  uint.MaxValue    + Zero
         * long or ulong   | -long.MaxValue   + Zero |  long.MaxValue    + Zero
         */
        [AttributeUsage(AttributeTargets.Field, AllowMultiple = true)]
        public class XAttribute : Attribute
        {
            public XAttribute(ulong Amplitude = 0, long Zero = 0) { }
        }

